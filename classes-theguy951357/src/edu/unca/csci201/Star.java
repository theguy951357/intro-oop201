package edu.unca.csci201;

public class Star {
	public static void star() {

		starTop();
		starBottom();
	}

	public static void starTop() {
		System.out.println("    /\\   ");
		System.out.println("   /  \\    ");
		System.out.println("---    ---    ");

	}

	public static void starBottom() {
		System.out.println("  \\    /   ");
		System.out.println("   //\\\\    ");
		System.out.println("  /    \\   ");

		System.out.println();

	}
}
